package Day09.JavaOperators;

public class JavaModul {
    public static void main(String[] args) {
        // Modul kalanı verir.

        System.out.println("10%3 ="+ 10%3); // 10 un 3 e bölümünden kalanı verir.
        System.out.println("3%10 ="+ 3%10);  // modul işleminde sol taraf sağdan küçükse sonuç sol taraftaki sayıdır...

        System.out.println("11%2 ="+ 11%2);
        System.out.println("10%2 ="+ 10%2);
        // bir sayının 2ye göre modulunde sonuç 1 veya 0 dır...bu bize sayının çift mi tek mi olduğunu verebilir....
    }
}
