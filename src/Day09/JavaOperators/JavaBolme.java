package Day09.JavaOperators;

public class JavaBolme {
    public static void main(String[] args) {

        int a=10;
        int b=3;

        System.out.println("a/b = " + (a/b)); // int 2 sayısının bölümü sonun tam sayıdır.. ,3
        System.out.println("a/b = " + (double)a/b); // ondalıklı sonuç istiyorsak değişkenlerden birini double yapıyoruz
                                                      //hassaiyet 15 hane
        System.out.println("a/b = " + (float)a/b); //hassasiyet 7 hane

        double c=3.14;
        double d=2.1;

        System.out.println("c/d = " + (c/d)); //int değişkeninde c/d parantez içindeyse sonuç ondalıksız gelir.....









    }
}
