package Day12_Math_Random;

public class JavaMath5 {


    public static void main(String[] args) {

        int a=-12;
        int b=2;
        //abs mutlak değeri verir
        System.out.println("a nın mutlak değeri= " + Math.abs(a) );
        System.out.println("a ve b den büyük olan= " + Math.max(a,b));
        System.out.println("iki sayıdan  büyük olan= " + Math.max(43,99));
        System.out.println("2 sayıdan küçük olanı "+ Math.min(a,b));
        System.out.println("16 nın karekökü "+ Math.sqrt(16));
        System.out.println("2 nin 3.kuvveti "+ Math.pow(2,3));
        System.out.println("round 3.1  :"+ Math.round(3.1));
        System.out.println("round 2.5  :"+ Math.round(2.5));
        System.out.println("ceil of 2.9  :"+ Math.ceil(2.9));
        System.out.println("ceil of 7.1  :"+ Math.ceil(7.1)); // küsürata bakmadan bir üstteki tam sayı
        System.out.println("floor of 6.5  :"+ Math.floor(6.5));
        System.out.println("floor of 7.1  :"+ Math.floor(7.1)); // küsürata bakmadan bir alttaki tam sayı
        System.out.println("floor of 8.9  :"+ Math.floor(8.9));



    }
}
