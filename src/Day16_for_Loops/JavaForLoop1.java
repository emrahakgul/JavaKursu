package Day16_for_Loops;

public class JavaForLoop1 {
    public static void main(String[] args) {

        //while loop ve for farkÄ±
        int i=0;
        while (i<5){
            System.out.println("w_i " +  i);
            i++;
        }

        for (i=0; i<5; i++) {
            System.out.println("f_i " + i);
        }

    }
}
