package Day16_for_Loops;

public class ForLoopIslemler {
    public static void main(String[] args) {
        //0dan 10a 10 dahil kadar ekrana yazdır
        //sonra 10 dan sıfıra kadar

        for (int i=0; i<= 10; i++)
        {
            System.out.print("w_i "+ i);
        }
        for (int i=10; i>=0; i--)
        {
            System.out.println("f_i " + i);
        }
        }

}
